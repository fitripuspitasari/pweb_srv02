    <h2> PAPEDA </h2>

<p>Papua terkenal dengan wisata budaya dan alamnya. Tapi jangan salah, wisata kuliner di provinsi paling timur Indonesia itu juga punya kunier legendaris, yaitu papeda.
Papeda merupakan makanan berupa bubur sagu khas Papua yang biasanya disajikan dengan mubara atau ikan tongkol yang dibumbui dengan kunyit. Teman papeda ini biasa disebut ikan kuah kuning.
Papeda belakangan mencuat kembali tatkala Presiden Joko Widodo melakukan kunjungan ke Papua. Seperti sebelumnya, pada kunjungan kedelapan di Papua, Jokowi kembali menyantap papeda dengan kuah kuning. Dalam satu kunjungan, Presiden Jokowi singgah bersama rombongan untuk sekadar menikmati papeda dengan ikan kuah kuning, di Restoran Cendrawasih 66, Timika.
Papeda atau bubur sagu selain makanan pokok masyarakat Papua, juga masyarakat Maluku. Papeda terdapat di hampir semua daerah di Maluku dan Papua. Bahan utama papeda dibuat dari tepung sagu, tepung sagu itu dibuat dengan cara menekok batang pohon sagu atau rumbai.
Papeda yang telah diolah menjadi bubur akan berwarna putih dan bertekstur lengket menyerupai lem dengan rasa yang tawar. Meskipun penampilannya seperti lem namun papeda merupakan makanan yang kaya serat, rendah kolesterol dan cukup bernutrisi</p>

<h3> Bahan: </h3>
<ol>
<li>100 gr tepung sagu</li>
<li>1000 cc air</li>
<li>1/2 sdt garam</li>
<li>1/2 sdt gula</li>
</ol>

<h3> Cara Membuat:</h3>
<ol>
<li> Cairkan tepung sagu dengan 300 ml air</li>
<li> Tambahkan garam dan gula</li>
<li> Didihkan sisa air</li>
<li> Tuang air yang sudah mendidih ke dalam larutan tepung sagu, aduk perlahan sehingga sagu matang merata</li>
<li> Papeda dikatakan sudah matang jika sudah berwarna bening jika masih belum merata matangnya adonan bisa dimasak di atas api kecil sambil terus diaduk</li>
<li> Jika sudah bening angkat dan sajikan hangat</li>
</ol>

<h3>Resep Ikan Kuah Kuning Sebagai Pelengkap Papeda Bahan: </h3>
<ol>
<li> 1/2 kg ikan tongkol, bersihkan dan rendam dengan jeruk nipis dan garam</li>
<li> 1 buah jeruk nipis, peras</li>
<li> Kemangi, siangi</li>
<li> Cabe rawit hijau, buang batangnya</li>
<li> 2 sdm minyak untuk menumis</li>
<li> 1 batang sereh, memarkan</li>
<li> 2 lembar daun salam</li>
<li> 600 ml air matang</li>
</ol>

<h3> Bumbu yang dihaluskan: </h3>
<ol>
<li> 2 siung bawang putih</li>
<li> 4 buah bawang merah</li>
<li> 3 buah kemiri</li>
<li> 2 cm jahe</li>
<li> 2 cm kunyit</li>
<li> 1 sdm gula pasir</li>
<li> 1 sdt sdm garam</li>
</ol>

<h3> Cara membuatnya : </h3>
<ol>
<li> Tumis bumbu yang sudah dihaluskan beserta sereh dan salam hingga harum dan matang</li>
<li> Tambahkan air matang, masak hingga mendidih</li>
<li> Masukkan ikan, garam, gula dan cabe rawit, masak hingga ikan matang</li>
<li> Sesaat sebelum masakan diangkat, tambahkan daun kemangi dan jeruk nipis, aduk rata</li>
<li> Sajikan sebagai pelengkap papeda</li>
</ol>

